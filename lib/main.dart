                    );
                  }).toList(),
                ),
        ),
        Padding(
          padding: const EdgeInsets.all(16.0),
          child: ElevatedButton(
            onPressed: () {
              showDialog(
                context: context,
                builder: (context) {
                  String newNote = '';
                  return AlertDialog(
                    title: Text('إضافة ملاحظة'),
                    content: TextField(
                      onChanged: (value) => newNote = value,
                      decoration: InputDecoration(
                        hintText: 'أدخل ملاحظتك هنا',
