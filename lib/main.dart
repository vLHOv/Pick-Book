                      ElevatedButton(
                        onPressed: () {
                          if (newNote.isNotEmpty) {
                            context.read<MyAppState>().addNote(newNote);
                          }
                          Navigator.of(context).pop();
                        },
                        child: Text('إضافة'),
                      ),
                    ],
                  );
                },
              );
            },
            child: Text('إضافة ملاحظة'),
          ),
        ),
      ],
