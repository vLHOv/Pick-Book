              child: Text(
                appState.currentWord["word"]!,
                style: TextStyle(fontSize: 36, color: Colors.white),
                textAlign: TextAlign.center,
              ),
            ),
          ),
          SizedBox(height: 20),
          Row(
            mainAxisSize: MainAxisSize.min,
            children: [
              ElevatedButton.icon(
                onPressed: () {
                  showDialog(
                    context: context,
                    builder: (context) => AlertDialog(
                      title: Text(appState.currentWord["word"]!),
                      content: Column(
                        mainAxisSize: MainAxisSize.min,
                        children: [
                          Text(appState.currentWord["description"]!),
                          SizedBox(height: 10),
                          Text(
                            appState.currentWord["source"]!,
                            style: TextStyle(fontSize: 12, color: Colors.grey),
                          ),
                        ],
                      ),
                      actions: [
                        TextButton(
                          onPressed: () => Navigator.of(context).pop(),
                          child: Text('إغلاق'),
                        ),
                      ],
                    ),
                  );
                },
                icon: Icon(Icons.info_outline),
                label: Text('عرض التفاصيل'),
              ),
              SizedBox(width: 10),
              ElevatedButton(
                onPressed: appState.nextWord,
                child: Text('المعلومة التالية'),
              ),
              SizedBox(width: 10),
              ElevatedButton.icon(
                onPressed: () {
                  appState.toggleFavorite(appState.currentWord["word"]!);
                },
                icon: Icon(appState.isFavorite(appState.currentWord["word"]!)
                    ? Icons.favorite
                    : Icons.favorite_border),
                label: Text('إعجاب'),
              ),
            ],
          ),
          SizedBox(height: 20),
          Wrap(
            spacing: 10,
            children: appState.categories.keys.map((category) {
              return ElevatedButton(
                onPressed: () {
                  appState.setActiveCategory(
                      category,
                      category == "التاريخ"
                          ? Colors.red.shade100
                          : category == "العلوم"
                              ? Colors.green.shade100
                              : category == "الفلسفة"
                                  ? Colors.blue.shade100
                                  : Colors.purple.shade100);
                },
                child: Text(category),
              );
            }).toList(),
          ),
        ],
      ),
    );
